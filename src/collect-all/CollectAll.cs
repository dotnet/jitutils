using System.Collections.Generic;

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

namespace SuperPMICollection
{
   public class CollectAll
   {
      public static void Main(string[] args)
      {
         Utilities.GetInstance(loggingVerbose: true);
          
         List<ISuperPMITask> tasks = new List<ISuperPMITask>();
         
         // All the superPMI Collection tasks
         tasks.Add(new CoreCLRSPMITask());
         tasks.Add(new CrossGenTask());
         
         // Run each task.
         foreach (var task in tasks)
         {
            task.RunTask();
         }
      }
   }
}
